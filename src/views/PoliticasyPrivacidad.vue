<h2> CONSIDERANDO</h2>
<p>Que la Constitución política Nacional de Colombia de 1991 establece en su artículo 15. "Todas las personas tienen derecho a su intimidad personal y familiar y a su buen nombre, y el Estado debe respetarlos y hacerlos respetar. De igual modo, tienen derecho a conocer, actualizar y rectificar las informaciones que se hayan recogido sobre ellas en bases de datos y en archivos de entidades públicas y privadas. En la recolección, tratamiento y circulación de datos se respetarán la libertad y demás garantías consagradas en la Constitución. (...)"
Que la Ley Estatutaria 1581 de 2012, por la cual se dictan disposiciones generales para la protección de datos personales, en el artículo 18 literal “f) adoptar un manual interno de políticas y procedimientos para garantizar el adecuado cumplimiento de la presente ley y, en especial, para la atención de consultas y reclamos por parte de los titulares;".
Que según lo estipulado en el Decreto 1377 de 2013 por el cual se reglamenta parcialmente la Ley 1581 de 2012, en su artículo 13; establece la información mínima requerida para el desarrollo de la política y procedimiento de tratamiento de datos.
</p>
<h2>   
    RESUELVE
</h2>
<p>
    Artículo 1. Adoptar la política y procedimiento para el tratamiento y protección de datos personales de los miembros de la comunidad del SENA, entendiendo por estos a los estudiantes, docentes, egresados, empleados, y contratistas de la institución; de igual forma se asimilarán como usuarios de la presente política a, los visitantes, conferencistas, proveedores y ex empleados que registren su información personal en las bases de datos de la institución, de conformidad con lo estipulado en la Ley 1581 de 2012 y sus decretos reglamentarios.

</p>
<p>
    Artículo 2. OBJETO. Garantizar la protección de los datos personales datos recolectados o que en el futuro se obtengan en el desarrollo de las actividades académicas, culturales, financieras y otras, de la comunidad del SENA, que reposen en las bases de datos, redes sociales y archivos de tal manera que el SENA proporcionará los medios para garantizar y no vulnerar los derechos de la privacidad, la intimidad personal y el buen nombre, de conformidad con el artículo 15 de la Constitución Política.  
</p>
<p>
    Artículo 3. DEFINICIONES. Para efectos de la presente política, se entiende por:
Autorización: Consentimiento previo, expreso e informado del Titular para llevar a cabo el Tratamiento de datos personales.
Base de Datos: Conjunto organizado de datos personales que sea objeto de Tratamiento.
Datos personales: Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.
Encargado del Tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta del responsable del Tratamiento.
      Responsable del Tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, decida sobre la base de datos y/o el tratamiento de la información.
f.     Titular: Persona natural cuyos datos personales sean objeto de Tratamiento.
Tratamiento: Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión.

</p>
<p> Artículo 4. PRINCIPIOS PARA EL TRATAMIENTO DE DATOS PERSONALES. El Sena CTPI acudirá para el desarrollo, interpretación y aplicación de su Política de Protección de datos a los siguientes principios, a los cuales se:
Principio de legalidad en materia de Tratamiento de datos: El Tratamiento a que se refiere la ley 1581 de 2012, es una actividad reglada que debe sujetarse a lo establecido en esta ley y en las demás disposiciones que la desarrollen.
Principio de finalidad: El Tratamiento debe obedecer a una finalidad legítima de acuerdo con la Constitución y la Ley, la cual debe ser informada al Titular. En lo correspondiente a la recolección de datos personales, EL SENA se limitará a aquellos datos que sean pertinentes y adecuados para la finalidad con la cual fueron solicitados o almacenados; la Rectoría, Secretaría, Direcciones, Unidades, Áreas, Divisiones, Secciones, y otras unidades organizacionales, deberán informar previamente al titular el motivo por el cual se solicita la información y el uso específico que se le dará a la misma.
Principio de libertad: El Tratamiento sólo puede ejercerse con el consentimiento, previo, expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados sin previa autorización, o en ausencia de mandato legal o judicial que releve el consentimiento.
Principio de veracidad o calidad: La información sujeta a Tratamiento debe ser veraz, completa, exacta, ser actualizada, comprobable y comprensible. Se prohíbe el Tratamiento de datos parciales, incompletos, fraccionados o que induzcan a error.
Principio de transparencia: En el Tratamiento debe garantizarse el derecho del Titular a obtener del responsable del Tratamiento o del Encargado del Tratamiento, en cualquier momento y sin restricciones, información acerca de la existencia de datos que le conciernan.
Principio de acceso y circulación restringida: El Tratamiento se sujeta a los límites que se derivan de la naturaleza de [os datos personales, de las disposiciones de la ley y la Constitución. En este sentido, el Tratamiento sólo podrá hacerse por personas autorizadas por el Titular y/o por las
personas previstas en la ley.
Los datos personales, salvo la información pública, no podrán estar disponibles en internet u otros medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente controlable para brindar un conocimiento restringido sólo a los titulares o terceros autorizados conforme a la ley.
Principio de seguridad: La información sujeta a Tratamiento por el SENA se deberá manejar con las medidas técnicas, humanas y administrativas que sean necesarias para otorgar seguridad a los registros evitando su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
Principio de confidencialidad: El Sena tiene el compromiso de garantizar la reserva de la información, inclusive después de finalizada su relación con alguna de las labores que comprende el Tratamiento, pudiendo sólo realizar suministro o comunicación de datos personales cuando ello corresponda al desarrollo de las actividades autorizadas en la ley y en los términos de la misma.
